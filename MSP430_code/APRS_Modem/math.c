/* power function */
unsigned long long pow(unsigned long long base, unsigned long long exp) {
    unsigned long long result = 1;
    unsigned long long i;

    for(i=0;i<exp;i++) {
        result *= base;
    }

    return result;
}

void set_bit(char* c, const unsigned char n, const unsigned char val) {
    if ((val & 0x01) == 0x01) {
        *c |= (0x01 << n); //set
    }
    else {
        *c &= ~(0x01 << n); //clear
    }
}

char get_bit(char c, const unsigned char n) {
    return (c >> n) & 0x01;
}

unsigned char sine(unsigned int index) {
    return sine_LUT[index];
}

/* multiply two fixed point numbers (DON'T USE) */
int FXP_mul(int a, int b) {
    long result = a * b;
    return (int)(result >> 8);
}

/* fast fixed point multiplication using the hardware multiplier */
int FXP_mul_hardware(int a, int b) {
    MPYS = a; //load operands
    OP2 = b;

    __no_operation(); //let multiplier finish

    return ((RES1 & 0x00FF) << 8) | ((RES0 >> 8) & 0x00FF);
}

